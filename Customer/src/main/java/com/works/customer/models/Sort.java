package com.works.customer.models;

public class Sort {
    private boolean empty;
    private boolean sorted;
    private boolean unsorted;
    public boolean getEmpty() { return empty; }
    public void setEmpty(boolean value) { this.empty = value; }
    public boolean getSorted() { return sorted; }
    public void setSorted(boolean value) { this.sorted = value; }
    public boolean getUnsorted() { return unsorted; }
    public void setUnsorted(boolean value) { this.unsorted = value; }
}